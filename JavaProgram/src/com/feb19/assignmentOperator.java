package com.feb19;

public class assignmentOperator {
	public static void main (String args[]) {
		int num1 = 20;
		int num2 = 5;
		num1+=num2; //num1=num1+num2
		System.out.println("Print Value:"+num1);
		num1-=num2; //num1=num1-num2
		System.out.println("Print Value:"+num1);
		num1*=num2;
		System.out.println("Print Value:"+num1);
		num1/=num2;
		System.out.println("Print Value:"+num1);
	}

}
